# Exercice 7: niveau intermediaire
def enleveDoublon(liste):
    ma_liste = []
    for i in liste:
        if i not in ma_liste:
            ma_liste.append(i)
    return ma_liste

liste = [23, 45, 23, 43, 7, 66, 21, 45, 23, 7, 200, 200]
print(enleveDoublon(liste))
        
